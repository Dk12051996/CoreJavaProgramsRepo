package Looping;

public class Type6 {

	public static void main(String[] args) {
		// from 1 to 100 ; find which odd and even
		
		for(int i=0 ; i<=100 ; i++)
			if(i%2==0)
			{
				System.out.println(i + "- It is Even Number");
			}
			else
			{
		       System.out.println(i + "- It is Odd Number");
			}
	}
}

	


